"""

A calculater module

"""


def add(a, b):
    """Add two number"""
    return a + b


def sub(a, b):
    """Subtract two number"""
    return a - b


def mul(a, b):
    """Multiply two number"""
    return a * b


def div(a, b):
    """Divide two number"""
    return a / b


def power(a, b):
    """Power two number"""
    return a**b
